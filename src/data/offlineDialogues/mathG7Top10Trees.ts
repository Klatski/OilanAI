import type { ReflectionFeedbackBlocks } from "../../types";
import { buildLinearOfflineTree, type LinearSegment } from "./buildLinearTree";

/** Частые такие ответы — все ведут к одному следующему узлу (линейный сценарий). */
function pick3(a: string, b: string, c: string): LinearSegment["choices"] {
  return [
    { label: a, keywords: [] },
    { label: b, keywords: [] },
    {
      label: c,
      keywords: [
        "не знаю",
        "не понимаю",
        "другое",
        "не могу",
        "формулирую",
        "своими",
      ],
    },
  ];
}

function rf(partial: ReflectionFeedbackBlocks): ReflectionFeedbackBlocks {
  return partial;
}

function lesson(
  topicId: string,
  segments: LinearSegment[],
  reflection: ReflectionFeedbackBlocks
) {
  return buildLinearOfflineTree(topicId, segments, reflection);
}

