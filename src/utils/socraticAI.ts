const SOCRATIC_QUESTIONS = [
  "А как ты думаешь, почему это работает именно так?",
  "Давай разберём по шагам — что происходит в самом первом действии?",
  "Если бы ты объяснял это младшему брату, с чего бы начал?",
  "Какой здесь ключевой объект, от которого всё зависит?",
  "А что изменится, если убрать одно из условий? Почему?",
  "Можешь привести аналогию из жизни? Где ты встречал похожее?",
  "Что тебе уже знакомо в этой задаче, а что совсем новое?",
  "Какая связь между тем, что ты сейчас сказал, и исходным вопросом?",
  "Попробуй сформулировать это своими словами — как ты видишь структуру?",
  "А если ошибёшься — как ты это заметишь? Есть способ себя проверить?",
];

const SOCRATIC_HINTS = [
  "Хм, интересно. Но не спеши — давай копнём глубже.",
  "Почти! Но одна деталь осталась незамеченной.",
  "Отличная интуиция. Теперь попробуй обосновать её строго.",
  "Ты идёшь в правильном направлении. Что будет следующим шагом?",
  "А что если посмотреть на это с другой стороны?",
];

const ENCOURAGE = [
  "💡 Молодец, что думаешь сам — это ценнее любого готового ответа.",
  "🌱 Ошибка здесь — это рост. Пробуй смело.",
  "⚡ Ты уже ближе, чем кажется. Держи мысль.",
];

function rand<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export interface SocraticContext {
  topic: string;
  knowledge: string;
  turn: number;
}

export function generateSocraticReply(
  userMessage: string,
  ctx: SocraticContext
): string {
  const clean = userMessage.trim().toLowerCase();

  if (clean.length < 8) {
    return "Попробуй раскрыть мысль чуть подробнее — мне важно понять, как ты рассуждаешь. Что ты имеешь в виду?";
  }

  if (/(не знаю|не понимаю|помоги|подскажи|реши|ответ)/.test(clean)) {
    return `${rand(ENCOURAGE)} Но готовый ответ я тебе не дам — вместо этого подскажу направление. ${rand(
      SOCRATIC_QUESTIONS
    )}`;
  }

  if (ctx.turn >= 4) {
    return `Отлично, ты уже проделал серьёзную работу мышления! Давай подытожим: попробуй сам сформулировать вывод по теме «${ctx.topic}». Что главное ты понял?`;
  }

  const hint = rand(SOCRATIC_HINTS);
  const question = rand(SOCRATIC_QUESTIONS);

  return `${hint} ${question}`;
}

export function generateReflectionFeedback(reflection: string): string {
  const len = reflection.trim().length;
  if (len < 20) {
    return "Хорошее начало, но мысль можно раскрыть шире. Попробуй описать не только ЧТО ты понял, но и КАК пришёл к этому пониманию — именно это закрепляет знание надолго. 🌱";
  }
  if (len < 80) {
    return "Отличная рефлексия! Ты чётко обозначил ключевые идеи. Попробуй в следующий раз добавить пример из жизни — это создаёт нейронные связи в два раза прочнее. ⚡";
  }
  return "Потрясающая глубина рефлексии! 🌟 Ты не просто запомнил — ты встроил знание в свою картину мира. Именно так думают исследователи. Продолжай в том же духе.";
}
